class Employee():
    def __init__(self, employee_id=0, name=None, username=None, password=None, email=None, role_id=0, salary=0, employee_role=None) -> None:
        self.employee_id = employee_id
        self.name = name
        self.username = username
        self.password = password
        self.email = email
        self.role_id = role_id
        self.salary = salary
        self.role = employee_role
        



